    private fun loadDictionary(fileName: String): Dictionary {
        val trie = TrieDictionary()
        try {
            val stream = appContext.assets.open("dict/$fileName")
            val reader = BufferedReader(InputStreamReader(stream, "UTF-8"))
            reader.use { r ->
                var line: String?
                while (r.readLine().also { line = it } != null) {
                    line = line?.trim() ?: continue
                    if (line.startsWith("#") || line.isEmpty()) continue
                    val parts = line.split("\t")
                    if (parts.size >= 2) {
                        val code = parts[0]
                        val word = parts[1]
                        trie.insert(code, word)
                    }
                }
            }
        } catch (_: Exception) {
            loadBuiltinDictionary(fileName, trie)
        }
        return trie
    }

    private fun loadBuiltinDictionary(fileName: String, dict: TrieDictionary) {
        when (fileName) {
            "pinyin.dict" -> loadBuiltinPinyin(dict)
            "wubi.dict" -> loadBuiltinWubi(dict)
            "english.dict" -> loadBuiltinEnglish(dict)
        }
    }

